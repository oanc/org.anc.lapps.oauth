package org.anc.lapps.oauth.web;

/**
 * @author Keith Suderman
 */
public class StringData
{
	protected String value;

	public StringData()
	{

	}

	public StringData(String value)
	{
		this.value = value;
	}

	public String getValue()
	{
		return value;
	}

	public void setValue(String value)
	{
		this.value = value;
	}
}
